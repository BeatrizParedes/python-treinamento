"Escreva um programa que declare uma variável inteira"
"Aplique o operador de incremento (usando +=1)"
"E decremento (usando -=1)"
"E exiba o valor da variável antes e depois de cada operação"

def incremento_decremento():
    variavel=10
    print(variavel)

    variavel+=1
    print(variavel)

    variavel-=1
    print(variavel)

if __name__=="__main__":
    incremento_decremento()